import  streamlit as st
import joblib
import numpy as np

model=joblib.load('churn_model.pkl')
# App title
st.title("Customer churn Prediction App")
st.markdown("Enter customer details below to predict the likelihood of churn.")
# User input sliders
total_charges=st.number_input("Total Charges (₹)", min_value=0.0,max_value=10000.0,value=2000.0)
monthly_charges=st.number_input("Monthly Charges (₹)",min_value=0.0,max_value=1000.0,value=100.0)
tenure=st.slider("Tenure (months)",min_value=0,max_value=72,value=12)

if st.button("predict churn"):
  input_data=np.array([[total_charges, monthly_charges, tenure]])
  prediction=model.predict(input_data)[0]

  if prediction ==1:
    st.error("customer is likely to churn")

  else:
    st.success("customer is likely to stay")







